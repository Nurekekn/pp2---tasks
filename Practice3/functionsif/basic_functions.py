
# 1. Простая функция
def greet():
    print("Hello!")

greet()

# 2. Функция с параметром
def greet_user(name):
    print("Hello,", name)

greet_user("Alex")

# 3. Функция сложения
def add(a, b):
    print(a + b)

add(5, 3)

# 4. Функция без параметров
def show_message():
    print("Python is awesome!")

show_message()